/*
---------------
JAVA VARIABLES:
---------------

* A variable (in any programming language) can be described as a named container that stores a piece of data in memory.
* SYNTAX: data-type *identifier-name* = initialized value;
* EXAMPLE: String fName = "James";
* Naming convention standard used for Java: CAMEL CASE
*/

public class JavaVariables {
    public static void main(String[] args) {
        String fName = "Kyle"; // variable 'fName' assigned the value "Kyle", of data type String
        int age = 25; // variable 'age' assigned the value 25, of data type int (integer)
        System.out.println("Hi I'm " + fName + " and I'm " + age + " years old!"); // string concatenation to display the output
    }
}

